    class Program
    {

        public static void Main()
        {
            Console.ForegroundColor = ConsoleColor.Cyan;
            Console.WriteLine("Задача 14. Проверка дня недели для 2024 года");
            Console.ResetColor();
            int day = 0, month = 0, m;
            string s;
            ConsoleKeyInfo e;
            Console.Write("Для запуска нажмите любую клавишу (Esc - прервать)");
            e = Console.ReadKey(true);
            if (e.Key == ConsoleKey.Escape)
            {
                MyInput.Exit();
            }
            int[] arrayOfDays = new int[12] {31, 29, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
            Console.WriteLine();
            bool check = true;
            do
            {
                day = MyInput.Int32("Введите день: ", "+", check);
                check = false;
            }
            while (day > 31);
            check = true;
            do
            {
                month = MyInput.Int32("Введите месяц: ", "+", check);
                if (month > 12 || day > arrayOfDays[month - 1])
                {
                    check = false;
                }
                else check = true;
            }
            while (!check);
            int dayOfWeek = 0;
            for (int i = 0; i < month - 1; i++) 
            {
                for (int j = 0; j < arrayOfDays[i]; j++)
                {
                    dayOfWeek++;
                    if (dayOfWeek == 7) dayOfWeek = 0;
                }
            }
            for (int i = 0; i < day - 1; i++)
            {
                dayOfWeek++;
                if (dayOfWeek == 7) dayOfWeek = 0;
            }
            Console.Write("{0}.{1}.2024 - это ", day, month);
            switch (dayOfWeek)
            {
                case 0: Console.WriteLine("Понедельник"); break;
                case 1: Console.WriteLine("Вторник"); break;
                case 2: Console.WriteLine("Среда"); break;
                case 3: Console.WriteLine("Четверг"); break;
                case 4: Console.WriteLine("Пятница"); break;
                case 5: Console.WriteLine("Суббота"); break;
                case 6: Console.WriteLine("Воскресенье"); break;
            }
            MyInput.Exit();
        }
    }
